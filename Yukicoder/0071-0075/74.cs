    public class Solver
    {
        public void Solve()
        {
            var n = sc.Integer();
            var d = sc.Integer(n);
            var w = sc.Integer(n);
            var set = new DisjointSet(n);
            for (int i = 0; i < n; i++)
            {
                set.positive[i] += w[i];
                var pos = (i + d[i]) % n;
                var neg = (i - d[i] + n * 1000000) % n;
                if (pos == neg) set.ok[pos] = true;
            }
            for (int i = 0; i < n; i++)
            {
                var pos = (i + d[i]) % n;
                var neg = (i - d[i] + n * 1000000) % n;
                set.Unite(pos, neg);
            }
            for (int i = 0; i < n; i++)
            {
                if (set[i] != i) continue;
                var cnt = set.Size(i);
                var pos = set.Count(i);
                if ((cnt - pos) % 2 == 0 || set.Ok(i))
                    continue;
                IO.Printer.Out.WriteLine("No");
                return;
            }
            IO.Printer.Out.WriteLine("Yes");
        }
        public IO.StreamScanner sc = new IO.StreamScanner(Console.OpenStandardInput());
        static T[] Enumerate<T>(int n, Func<int, T> f) { var a = new T[n]; for (int i = 0; i < n; ++i) a[i] = f(i); return a; }
        static public void Swap<T>(ref T a, ref T b) { var tmp = a; a = b; b = tmp; }
    }
