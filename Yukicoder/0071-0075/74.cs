public class DisjointSet
{
    public int[] par, ranks, count, positive;
    public bool[] ok;
    public DisjointSet(int n)
    {
        par = new int[n];
        count = new int[n];
        ok = new bool[n];
        positive = new int[n];
        for (int i = 0; i < n; i++)
        {
            par[i] = i;
            count[i] = 1;
        }
        ranks = new int[n];
    }
    public int this[int id] { get { return (par[id] == id) ? id : this[par[id]]; } }
    public bool Unite(int x, int y)
    {
        x = this[x]; y = this[y];
        if (x == y) return false; 
        if (ranks[x] < ranks[y])
        {
            par[x] = y;
            ok[y] |= ok[x];
            count[y] += count[x];
            positive[y] += positive[x];
        }
        else
        {
            par[y] = x;
            positive[x] += positive[y];
            count[x] += count[y];
            ok[x] |= ok[y];
            if (ranks[x] == ranks[y])
                ranks[x]++;
        }
        return true;
    }
    public int Size(int x) { return count[this[x]]; }
    public int Count(int x) { return positive[this[x]]; }
    public bool Ok(int x) { return ok[this[x]]; }
    public bool IsUnited(int x, int y) { return this[x] == this[y]; }

}
