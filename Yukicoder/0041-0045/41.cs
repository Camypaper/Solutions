        public void Solve()
        {
            var T = sc.Integer();
            var map = new HashMap<long, ModInteger>();

            const long add = 111111;
            for (long i = 0; i <= (long)1e10; i += add)
                map[i] = 1;
            for (int _ = 1; _ < 10; _++)
            {
                var v = _ * add;
                for (long i = v; i <= (long)1e10; i += add)
                    map[i] += map[i - v];
            }

            for (int i = 0; i < T; i++)
            {
                var M = sc.Long();
                M -= M % 111111;
                IO.Printer.Out.WriteLine(map[M]);
            }
        }

